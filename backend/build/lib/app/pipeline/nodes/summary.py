from __future__ import annotations

import asyncio
import json
from typing import Any

from app.database import SessionLocal
from app.models.output import JobOutput
from app.pipeline.state import PipelineState
from app.prompts.summary import build_redflag_narrative_prompt, build_summary_prompt
from app.schemas.summary import MANDATORY_DISCLAIMER, UnderwriterSummary
from app.services.gemini_client import get_gemini_client

LARGE_LOSS_THRESHOLD = 25000.0


def _all_claims(claims_array: dict | None) -> list[dict]:
    claims: list[dict] = []
    for period in (claims_array or {}).get("policy_periods", []):
        claims.extend(period.get("claims", []))
    return claims


def _to_float(value: Any) -> float:
    try:
        return float(value)
    except (TypeError, ValueError):
        return 0.0


def _fallback_summary(
    *,
    job_id: str,
    insured_name: str,
    years_analyzed: list[int],
    analytics: dict,
    red_flags: list[dict],
    large_losses: list[dict],
    open_claims: list[dict],
) -> UnderwriterSummary:
    years_txt = ", ".join(str(y) for y in sorted(years_analyzed)) if years_analyzed else "N/A"
    overall_lr = analytics.get("overall_loss_ratio")
    overall_lr_txt = f"{_to_float(overall_lr) * 100:.2f}%" if overall_lr is not None else "N/A"

    return UnderwriterSummary(
        job_id=job_id,
        executive_summary=(
            f"Loss history for {insured_name} was analyzed across years {years_txt}. "
            f"The overall reported loss ratio is {overall_lr_txt} based on available premium and incurred values."
        ),
        year_by_year=(
            "Yearly results were evaluated for claim count, incurred loss, frequency, and severity trends. "
            "Review yearly statistics for exact numeric movement by period."
        ),
        large_loss_detail=(
            "Large losses were identified using the deterministic threshold above $25,000. "
            f"Total large-loss claims identified: {len(large_losses)}."
        ),
        open_claim_status=(
            "Open-claim exposure was reviewed using current reserve balances and claim status. "
            f"Total open claims identified: {len(open_claims)}."
        ),
        red_flag_disclosure=(
            "Red flags are generated by deterministic rules. "
            f"Total confirmed flags: {len(red_flags)}."
        ),
        risk_management_observations=(
            "Observed risk signals are based on structured claim outcomes and deterministic analytics. "
            "This narrative intentionally avoids coverage recommendations."
        ),
        disclaimer=MANDATORY_DISCLAIMER,
    )


def _build_main_summary(
    *,
    job_id: str,
    insured_name: str,
    analytics: dict,
    red_flags: list[dict],
    large_losses: list[dict],
    open_claims: list[dict],
) -> UnderwriterSummary:
    client = get_gemini_client()
    prompt = build_summary_prompt(
        insured_name=insured_name,
        years_analyzed=analytics.get("years_analyzed", []),
        yearly_stats=analytics.get("yearly_stats", []),
        red_flags=red_flags,
        overall_loss_ratio=analytics.get("overall_loss_ratio"),
        frequency_trend=analytics.get("frequency_trend"),
        severity_trend=analytics.get("severity_trend"),
        large_losses=large_losses,
        open_claims=open_claims,
    )

    text = asyncio.run(client.generate_text(prompt))
    try:
        payload = json.loads(text)
        payload["job_id"] = job_id
        payload["disclaimer"] = MANDATORY_DISCLAIMER
        return UnderwriterSummary.model_validate(payload)
    except Exception:
        return _fallback_summary(
            job_id=job_id,
            insured_name=insured_name,
            years_analyzed=analytics.get("years_analyzed", []),
            analytics=analytics,
            red_flags=red_flags,
            large_losses=large_losses,
            open_claims=open_claims,
        )


def summary_node(state: PipelineState) -> PipelineState:
    job_id = state["job_id"]
    insured_name = state.get("insured_name", "")
    claims_array = state.get("claims_array") or {}
    analytics = state.get("analytics") or {}
    red_report = state.get("red_flags") or {"flags": []}
    flags = list(red_report.get("flags", []))

    # 3.1: generate one narrative sentence per confirmed red flag.
    client = get_gemini_client()
    enriched_flags: list[dict] = []
    for flag in flags:
        prompt = build_redflag_narrative_prompt(flag)
        narrative = asyncio.run(client.generate_text(prompt)).strip()
        words = narrative.split()
        narrative = " ".join(words[:100])
        updated = dict(flag)
        updated["narrative"] = narrative
        enriched_flags.append(updated)

    red_report = {**red_report, "flags": enriched_flags}

    claims = _all_claims(claims_array)
    large_losses = [
        c for c in claims if _to_float(c.get("amount_incurred")) > LARGE_LOSS_THRESHOLD
    ]
    open_claims = [c for c in claims if str(c.get("status", "")).lower() == "open"]

    summary = _build_main_summary(
        job_id=job_id,
        insured_name=insured_name,
        analytics=analytics,
        red_flags=enriched_flags,
        large_losses=large_losses,
        open_claims=open_claims,
    )

    summary_json = summary.model_dump_json()

    db = SessionLocal()
    try:
        output = db.query(JobOutput).filter(JobOutput.job_id == job_id).first()
        if output:
            output.draft_summary = summary_json
            output.redflags_json = json.dumps(red_report, default=str)
            db.commit()
    finally:
        db.close()

    return {
        **state,
        "red_flags": red_report,
        "draft_summary": summary_json,
        "final_summary": state.get("final_summary") or summary_json,
        "current_stage": "summary",
    }
